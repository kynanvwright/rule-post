import 'package:flutter/material.dart';

// Import your screens
import 'auth/screens/login_screen.dart';
import 'content/screens/placeholder.dart';
import 'package:firebase_core/firebase_core.dart';
import 'firebase_options.dart'; // ðŸ‘ˆ this is generated by flutterfire configure


void main() async {
  WidgetsFlutterBinding.ensureInitialized();
  await Firebase.initializeApp(
    options: DefaultFirebaseOptions.currentPlatform,
  );
  runApp(const MyApp());
}


class MyApp extends StatelessWidget {
  const MyApp({super.key});

  @override
  Widget build(BuildContext context) {
    return MaterialApp(
      debugShowCheckedModeBanner: false,
      title: 'Rule Enquiries App',

      // ðŸ‘‡ This decides which screen shows first
      initialRoute: '/login',

      // ðŸ‘‡ Define your appâ€™s routes
      routes: {
        '/login': (context) => const LoginScreen(),
        '/home': (context) => const NewsFeedPage2(),
      },
    );
  }
}

